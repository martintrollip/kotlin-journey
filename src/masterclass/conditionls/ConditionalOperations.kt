fun main(args: Array<String>) {
   /* > <  == != <= >= */
    val a = 10
    val b = 20

    println("$a > $b ${a > b}")
    println("$a < $b ${a < b}")
    println("$a == $b ${a == b}")
    println("$a != $b ${a != b}")
    println("$a <= $b ${a <= b}")
    println("$a >= $b ${a >= b}")
}